function resolve(obj, path, vars) {
  const parts = path.split('.')
  let node = obj
  for (const p of parts) {
    if (!node || typeof node !== 'object') return undefined
    node = node[p]
  }
  if (typeof node === 'string' && vars) {
    return node.replace(/\{(\w+)\}/g, (_, k) => (vars[k] ?? ''))
  }
  return node
}

export const I18nContext = React.createContext({ lang: 'zh-CN', t: (k, v) => resolve(dict['zh-CN'], k, v) || k })

export function I18nProvider({ lang = 'zh-CN', children }) {
  const t = useCallback((key, vars) => {
    return resolve(dict[lang] || dict['zh-CN'], key, vars) || resolve(dict['zh-CN'], key, vars) || key
  }, [lang])
  // 避免在 .js 中使用 JSX（Vite 默认要求 .jsx 后缀），改用 createElement
  return React.createElement(I18nContext.Provider, { value: { lang, t } }, children)
}

export function useI18n() {
  return useContext(I18nContext)
}
